async function main() {
  const account = await prisma.account.findFirst({
    where: { slug: "beauty-salon" },
    select: { id: true },
  });

  if (!account) {
    throw new Error("Account with slug 'beauty-salon' not found");
  }

  for (const doc of docs) {
    const record = await prisma.legalDocument.upsert({
      where: {
        accountId_key: {
          accountId: account.id,
          key: doc.key,
        },
      },
      create: {
        accountId: account.id,
        key: doc.key,
        title: doc.title,
        description: doc.description,
        isRequired: doc.isRequired,
        sortOrder: doc.sortOrder,
      },
      update: {
        title: doc.title,
        description: doc.description,
        isRequired: doc.isRequired,
        sortOrder: doc.sortOrder,
      },
    });

    const lastVersion = await prisma.legalDocumentVersion.findFirst({
      where: { documentId: record.id },
      orderBy: { version: "desc" },
    });

    if (lastVersion && lastVersion.content === doc.content) {
      continue;
    }

    await prisma.legalDocumentVersion.updateMany({
      where: { documentId: record.id, isActive: true },
      data: { isActive: false },
    });

    const nextVersion = (lastVersion?.version ?? 0) + 1;

    await prisma.legalDocumentVersion.create({
      data: {
        documentId: record.id,
        version: nextVersion,
        content: doc.content,
        isActive: true,
      },
    });
  }

  console.log("Legal documents seeded.");
}

main()
  .catch((err) => {
    console.error(err);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });

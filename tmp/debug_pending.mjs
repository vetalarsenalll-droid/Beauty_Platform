import { PrismaClient } from '@prisma/client';
const prisma=new PrismaClient();
const t=await prisma.aiThread.findFirst({orderBy:{id:'desc'},select:{id:true}});
const msgs=await prisma.aiMessage.findMany({where:{threadId:t.id},orderBy:{id:'desc'},take:6,select:{role:true,content:true}});
const assistantLast=[...msgs].reverse().find(m=>m.role==='assistant')?.content??'';
console.log('thread',t.id);
console.log('assistantLast=',assistantLast);
console.log('m1',assistantLast.match(/подтвержда[\p{L}]*\s+отмен[\p{L}]*\s*#?\s*(\d{1,8})/iu)?.[1]);
console.log('m2',/(для\s+подтверждени[\p{L}]*|подтверд[\p{L}]*\s+отмен[\p{L}]*)/iu.test(assistantLast));
console.log('m3',assistantLast.match(/#\s*(\d{1,8})\b/)?.[1]);
await prisma.$disconnect();
